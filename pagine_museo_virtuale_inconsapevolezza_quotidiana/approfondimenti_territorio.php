            <article id="paragrafi_approfondimenti">

                        <p id="introduzione">
                        <h3>Introduzione</h3>
L’invenzione dell’agricoltura e dell’allevamento furono di estrema importanza per la storia dell’umanità  (vedi le sezioni “il mangiare” ed “effetti sulla società storia” di questo sito). Tuttavia, oggi, la grande varietà di cibo e la possibilità di acquistarlo fanno si che l’atto della scelta rappresenti anche la decisione di rafforzare o meno un sistema produttivo con un elevato impatto ambientale. Conoscere il processo di produzione degli alimenti, porta i consumatore a scegliere con maggior coscienza.  
                        </p>
                        
                        <p id="agricoltura_intensiva">
                        <h3>Agricoltura intensiva</h3>
La produzione intensiva ha come scopo principale il maggior lucro possibile, senza preoccuparsi delle conseguenze sociali e ambientali. In questo senso, una profonda mancanza in questo processo è data dalle monoculture (coltivazioni di una sola specie in uno stesso terreno) che, con il passare del tempo, privano il suolo delle sostanze fertili. In aggiunta, questi sistemi utilizzano fertilizzanti chimici e agrotossici per il controllo dei parasiti, mettendo a rischio la biodiversità e inquinando le falde acquifere. Spesso vengono anche utilizzati semi geneticamente modificati (OGM) che tendono a far estinguere le altre specie e non sempre l’irrigazione è ottimizzata, con un conseguente spreco d’acqua. La raccolta non rispetta la stagionalità, mentre la distribuzione avviene tramite le grandi reti di distribuzione organizzata, il che comporta un’elevata emissioni di Co<sub>2</sub>.  
                          </p>
                        
                        <p id="allevamento_intensivo">
                        <h3>Allevamento intensivo</h3>
Per molte culture, le feste e gli incontri sono una occasione per mangiare insieme agli altri e spesso la portata principale è la carne. Per soddisfare la grande richiesta, l’allevamento (principalmente bovino) cresce esponenzialmente e spesso a discapito delle foreste (per esempio come succede alla foresta Amazzonica in Brasile). La carne ha oltrepassato la linea della necessità proteica per diventare quasi come una dipendenza psicologica e rituale. Lo scopo principale della produzione in grande scala è massimizzare il lucro e con la minor spesa possibile, iniziando dalle scelta della razza di bestiame, generalmente ibride per ottenere una maggiore massa corporea. Durante la vita degli animali, la loro alimentazione è costituita da mangime industriale, mentre lo spazio procapite è notevolmente ridotto rispetto al necessario, compromettendo la salute. Per quanto riguarda l’approvvigionamento energetico e idrico non viene contemplato il riutilizzo delle risorse e tanto meno se le risorse provengono da fonti non rinnovabili.
                        </p> 

                        <p id="allevamento_intensivo">
                        <h3>Sistema produttivo non sostenibile</h3>
La distribuzione del cibo avviene affidata (nella maggior parte dei casi) alla GDO (Grande distribuzione organizzata). Per la conservazione i prodotti devono essere refrigerati a lungo (catena del freddo) e hanno bisogno di imballaggio.  L’emissione di CO2 è più elevata rispetto a una produzione sostenibile e nell’immediato il prodotto sembra avere un costo minore, ma, oltre a essere di bassa qualità, possiede un’elevata impronta ecologica e un notevole costo sociale. 
                        </p>
            
            
                        <p id="agricoltura_sostenibile">
                        <h3>Agricoltura sostenibile</h3>
La coltivazione del cibo in modo sostenibile ha come obbiettivo principale: produrre alimenti agroalimentari di qualità, valorizzare delle risorse del territorio, preservare la natura, impiego di mano d’opera e conseguente rispetto dei diritti dei lavoratori. Questo sistema produttivo tende a ottimizzare l’utilizzo delle risorse, creando così un sistema aperto, cioè che si auto sostiene. 
Per fertilizzare il suolo si utilizza concime naturale (ricavati dai rifiuti organici – come foglie secche, resti di cibo, frutti marci, strume dei bovini, ecc.), per la semina vengono utilizzati i semi naturali (non OGM*) e per il controllo dei parassiti, si ricorre all’inserimento di insetti e/o batteri antagonisti, senza che le caratteristiche del suolo e la biodiversità sia danneggiata. L’irrigazione sfrutta il riutilizzo dell’acqua piovana e l’irrigazione a goccia, ottimizzando lo spreco d’acqua e la raccolta rispetta il ciclo di vita e le stagionalità dei orti. Infatti, prima di rincominciare nuovamente a coltivare, il suolo passa prima per una fase di “riposo”, in modo da recuperare i nutrienti, garantendone la fertilità. 
Una volta raccolti i frutti (intesi in senso lato), si procede al controllo della qualità per valutarne dimensioni, freschezza, grado di maturazione, ecc. Generalmente, i prodotti di prima scelta vengono destinati alla vendita pubblica, mentre quelli che presentano difetti estetici e ammaccature sono inviati per la lavorazione di prodotti derivati (marmellate, succhi, ecc.). 
In merito alla distribuzione, i prodotti agricoli vengono trasportati preferibilmente i mercati locali, minimizzando così la emissioni di CO2. 
                        </p>
                        
                        <p id="allevamento_sostenibile">
                        <h3>Allevamento sostenibile</h3>
Allevamento sostenibile tende ad avere come meta la qualità del cibo, il rispetto delle risorse del territorio e massimo benessere dell’animale. Questo inizia con la scelta della razza (preferibilmente locale), mentre la quantità di capi rispetta le esigenze di spazio. Le strutture dedicate prevedono il recupero e il trattamento, sia dell’acque (reflue e meteoriche) e sia delle feci che possono essere utilizzate come concimi naturali e/o trasformate (tramite un biodigestore) in energia elettrica.<br /><br/>
						</p>

                        <p id="sistema_sostenibile">
                        <h3>Sistema produttivo sostenibile</h3>
La distribuzione del cibo avviene affidata entro un kilometraggio limitato al fine di preservare la qualità dei prodotti e di limitare l’impatto ambientale legato all’emissione di CO2 nell’atmosfera. Per quanto riguarda la conservazione dei prodotti, gli alimenti non necessitano di sostanze conservanti e di metodi di refrigerazione artificiali.  Apparentemente, il costo al pubblico del prodotto risulta essere maggiore. Tuttavia, questo può essere giustificato dall’evidente differenza di qualità con un prodotto derivato da un sistema non sostenibile e dal fatto che questi hanno un impatto ambientale notevolmente inferiore, abbattendo di conseguenza il costo soaciale. 
            			</p>
           
        
