<!-- Paleolitico -->
 
    <div id="intro_paleolitico" class="testo_societa">
        <span class="f_size16 bold testo_sinistra f_arancio">Paleolitico</span>
        <span class="f_size10 italic"> circa un milione di anni fa (principali avvenimenti – scoperta del fuoco, sviluppo di strumenti di pietra e organizzazione sociale in piccoli gruppi.)</span><br/><br/>    
            <p>
    		<span class="bold">Il nutrirsi era strettamente collegato a ci&ograve; che il territorio offriva.</span> Gli studiosi dell&rsquo;argomento<sup>1</sup> affermano che in questo periodo l&rsquo;alimentazione dell&rsquo;homo habilis<sup>2</sup> era onnivora e che probabilmente la loro dieta era composta da: semi, tuberi e radici di piante varie, termiti, cavallette, formiche, piccoli topi di campagna, uova di uccelli e di rettili e carogne degli animali.<sup>3</sup><br /><br /> 
Alcuni scienziati sostengono che <span class="bold">il cibarsi di carne abbia permesso all&rsquo;homo habilis (rispetto al suo predecessore che mangiava esclusivamente vegetali) di sviluppare maggiormente il proprio cervello.</span> Questo fu possibile non solo grazie alle proteine contenute, ma anche per la necessit&agrave; creata di inventare e costruire strumenti che permettessero di tagliare la carne. Di conseguenza si svilupp&ograve; il ragionamento astratto, dato dalla concettualizzazione del bisogno e dalla concretizzazione di una soluzione immaginata.<sup>4</sup>
    <span class="bold">Ci&ograve; fa riflettere: la carne &egrave; ancora indispensabile allo sviluppo umano?</span> 
            </p>
   
	</div>   

<!--Neolitico -->

    <div id="intro_neolitico" class="testo_societa">
        <span class="f_size16 bold testo_sinistra f_arancio">Neolitico</span><span class="f_size10 italic"> circa 800 anni fa (principali avvenimenti – Dominio del fuoco, Sviluppo dell’abilit&agrave; di caccia, caccia in gruppo, invenzione dell’agricoltura e l&rsquo;uomo che diventa sedentario).
        </span><br/><br />   
            <p>
Sicuramente <span class="bold">l&rsquo;invenzione dell&rsquo;agricoltura<sup>5</sup> rappresent&ograve; un grande passo avanti, sia per consolidare l&rsquo;organizzazione sociale sia per cambiare definitivamente la dieta dell&rsquo;uomo.</span> Questo port&ograve; anche l&rsquo;uomo a stabilirsi in posti fissi e pian piano le tecniche di coltivazione e lavorazione del cibo iniziarono un processo di sviluppo continuativo.
<span class="bold">Dominare il fuoco ovvero la possibilit&agrave; di poterlo accendere quando lo si desiderava, permise la preparazione di alimenti pi&ugrave; saporiti e durevoli nel tempo.</span> 
Si ipotizza che in questo periodo <span class="bold">l&rsquo;uomo si cibasse principalmente di cereali coltivati (orzo e ceci) e di animali allevati,</span> mentre cacciasse in un modo sempre pi&ugrave; simile ad uno &quot;sport&quot;, in quanto, grazie all’invenzione dell&rsquo;agricoltura, aveva a disposizione risorse alimentari, prima inesistenti o non sfruttate.
            </p>
    </div> 

<!--Mesopotamia -->

    <div id="intro_mesopotamia" class="testo_societa">
        <span class="f_size16 bold testo_sinistra f_arancio">Mesopotamia</span><span class="f_size10 italic"> III millennio a.C (principali avvenimenti – Invenzione della ruota e impiego nei trasporti</span><br /><br />    
        <p>
Nelle regioni della Mesopotamia, si svilupp&ograve; la civilt&agrave; dei Sumeri<sup>6</sup> ai quali, oltre al <span class="bold">dominio dell’acqua, si attribuiscono l’invenzione della ruota e il suo utilizzo nei trasporti<sup>7</sup>, l&ideazione dei primi attrezzi agricoli (come l&rsquo;<a class="link_sottolineato" href="http://www.treccani.it/enciclopedia/aratura/" target="_blank" title="aratro">aratro</a> a trazione animale e varie tipi di falci) e il perfezionamento dell’arte della <a class="link_sottolineato" href="http://www.treccani.it/enciclopedia/mietitura/" target="_blank" title="mietitura">mietitura</a>e della <a class="link_sottolineato" target="_blank" title="trebbiatura" href="http://www.treccani.it/enciclopedia/trebbiatura/">trebbiatura</a>.</span><br /><br />  
Facevano parte dei prodotti coltivati: cipolle, cetrioli, aglio, lenticchie, piselli, ceci, fave, farro, orzo, frumento, albicocche, datteri, fichi, mele, noci, pere, mandorle. 
Dai cereali si producevano <span class="bold">il pane e la birra che svolgevano (come nella maggior parte delle civilt&agrave; antiche) un ruolo importante nell&rsquo;alimentazione.</span>
I Sumeri allevavano gli asini, i maiali, le capre ed i bovini. Da quest&rsquo;ultimi ricavavano il latte e la “mano d’opera” per il lavoro della terra. L&rsquo;attrezzatura da cucina comprendeva pentole di terracotta, macine per schiacciare i cereali e forni, il tutto registrato su graffiti e/o scrittura. 
&Egrave; stato scoperto che i Sumeri festeggiavano i matrimoni, le costruzioni di templi, le vittorie militari ed altri eventi rilevanti per la loro vita sociale con grandi banchetti. 
        </p> 
    </div>  

<!-- egiziani -->

    <div id="intro_egiziani" class="testo_societa"><span class="f_size16 bold testo_sinistra f_arancio">Egizi</span><span class="f_size10 italic"> tra 4000 a.C  –  3500 a.C  (principali avvenimenti: conservazione degli alimenti, grandi costruzioni)</span><br/><br/>    
        <p>
Dalla terra fertile che il Nilo offriva, si ricavavano principalmente cereali<sup>11</sup>, fichi, cocomeri, meloni, datteri, uva; inoltre ortaggi come cipolle, porri, aglio, sedano, cetrioli e soprattutto ceci, fave e lenticchie che  furono &quot;elemento quotidiano dell&rsquo;alimentazione degli antichi egizi&quot;<sup>12</sup>. Il Nilo offriva i pesci come il muggine dalle cui uova ricavavano un tipo di bottarga.
In aggiunta, gli egizi potevano contare sulla carne derivante dall&rsquo;allevamento di capi bovini, caprini, suini e ovini.<sup>13</sup> La loro dieta, cos&igrave; come per i Sumeri, era basata principalmente su pane e birra.<sup>14</sup> 
L&rsquo;importanza del cibo per questa civilt&agrave; si evidenzia dal fatto che nelle tombe dei faraoni sono stati trovati contenitori alimentari che, come confermato dagli studi archeologici, servivano per conservare il cibo pi&ugrave; a lungo, in quanto si credeva potesse servire dopo la morte. 
		</p>
    </div>  

<!-- Greci -->

    <div id="intro_greci" class="testo_societa"><span class="f_size16 bold testo_sinistra f_arancio">Antica Grecia</span><span class="f_size10 italic"> 1000 a.C</span><br /><br />    
        <p>
Per i greci, il nutrirsi acquis&igrave; un significato ancora pi&ugrave; ampio, legato non solo all&rsquo;alimentazione del corpo, ma anche dello spirito. L&rsquo;archeologa M. Teresa Iannelli afferma che per gli antichi greci i pasti erano anche un momento di incontro perch&eacute; avevano un forte ruolo sociale, sottolineato dalle attivit&agrave; connesse come i banchetti (dove si mangiava), i simposi (dove si beveva), i rituali dell’ospitalit&agrave; e le feste sia civili che religiose. 
Il cibarsi era considerato un rituale da svolgere in modo particolare: &quot;(...)semisdraiati su letti conviviali (klinai), appoggiati al braccio sinistro, sostenuto da alcuni cuscini, accompagnati sempre al suono di musica e in una atmosfera di conversazione&quot;.<sup>15</sup> Il pane di frumento, l&rsquo;orzo, il vino e la birra erano gli alimenti che non mancavano nella loro dieta.  
Il loro menu era anche composto da alimenti derivati dai cereali, vegetali (cipolle, rape, cardi, asparagi, lattuga, pinoli), pesci, molluschi, frutti di mare e crostacei, frutta fresca (mele, pere, uva, melograni e fichi) e anche secca (mandorle, nocciole e noci), oltre a molto formaggio. 
La carne invece non aveva un ruolo centrale: se ne consumava un massimo di 2 kg all&rsquo;anno e solo nei giorni di festa. 
Si crede anche che alcuni insetti (come le cicale), implementassero la loro dieta.<sup>16</sup>
        </p><br /><br /> 
    </div>   


<!-- Romani -->

    <div id="intro_romani" class="testo_societa"><span class="f_size16 bold testo_sinistra f_arancio">Impero Romano </span><span class="f_size10 italic"> tra il III secolo a.C. e il V secolo d.C. </span><br /><br />     
        <p>
Anche per gli antichi romani gli alimenti derivati dai cereali avevano un ruolo centrale; in pi&ugrave;, una sorta di pappa di farro bollito con verdure era l&rsquo;alimento abituale tra la gente del popolo. 
Il pane e la birra, come per le altre civilt&agrave;, erano gli alimenti principali prodotti in grande variet&agrave; per tutti i gusti e classi sociali. Si coltivavano in abbondanza fichi e ulivi. 
Nel primo secolo d.C il cibo dei soldati romani era a base di lardo, formaggio e posca (acqua mista ad aceto), con gallette di grano. Altri pi&ugrave; privilegiati mangiavano prodotti sotto sale, salumi e carne.<sup>17</sup> 
I loro pasti erano generalmente tre al giorno: i primi due pi&ugrave; piccoli e l&rsquo;ultimo pi&ugrave; abbondante. L&rsquo;espansione continua dell&rsquo;impero romano ha portato, oltre ad un maggiore territorio a disposizione, ad un incorporamento di altre abitudini sia nella cultura che negli articoli alimentari.<sup>18</sup>
        </p>
    </div>   

<!--Mdioevo -->

    <div id="intro_medioevo" class="testo_societa">
        <span class="f_size16 bold testo_sinistra f_arancio">Medioevo</span><span class="f_size10 italic"> tra V secolo e XV secolo</span>
        <br /><br />
        <p>
Le abitudini alimentari nel medioevo si arricchirono con i grandi scambi culturali dovuti alla conquista romana. In Europa Meridionale si incominciarono a coltivare gli aranci ed i limoni, portati dagli arabi. Divent&ograve; un&rsquo;abitudine anche coltivare ortaggi come asparagi, carciofi, melanzane e spinaci. 
I pi&ugrave; poveri invece avevano una dieta scarsa, praticamente a base di pappe di cereali.<sup>19</sup> La base dell&rsquo;alimentazione era sempre il pane, con la birra, il vino ed il sidro. 
Lo sviluppo delle tecniche di conservazione del cibo assunse sempre maggiore importanza. Ad esempio, la conservazione degli alimenti con il sale contribu&igrave; all&rsquo;espansione marittima ed alla successiva colonizzazione delle Americhe.  
       </p>
    </div>   

<!--rinascimento -->

    <div id="intro_rinascimento" class="testo_societa"><span class="f_size16 bold testo_sinistra f_arancio">Dal 1500 al 1700</span><span class="f_size10 italic"> rinascimento, scoperta dell&rsquo;America, grande sviluppo scientifico</span><br /><br />  
        <p>
Con la scoperta del continente americano, molti prodotti come patate, mais, peperoni, peperoncini, fagioli, tacchini e cacao iniziarono ad avere una singolare importanza nella dieta europea.
Nel &rsquo;700 le autorit&agrave; incentivarono l&rsquo;utilizzo del mais per combattere la fame e la denutrizione, causate dalle continue guerre.<sup>20</sup> Oltre al mais, anche la patata divenne l’&rsquo;alimento principale dei cittadini pi&ugrave; poveri, in particolar modo nei territori corrispondenti alle attuali nazioni di Gran Bretagna, Olanda, Germania e Francia.<sup>21</sup>. 
In questo periodo la parte povera della popolazione soffriva la scarsa quantit&agrave; e variet&agrave; di cibo, mentre i nobili avevano un menu tipico, piuttosto ricco in quantit&agrave; e qualit&agrave;: &quot;come primo un piatto di carne bollita, seguono salsicce, torte di piccione e pernice, pollo in gelatina o quaglie. Il secondo: arrosto, uccelli, orecchie di porco, testicoli di vari animali, uova, cardi, carciofi, gelatine e carni bianche. Dolce: frutta e composte.&quot;.<sup>22</sup> La carne accessibile ai contadini era solo quella dei maiali, che erano nutriti con gli avanzi dei signori. 
		</p>
    </div>   
    
<!--tempi moderni -->
    <div id="intro_tempi_moderni" class="testo_societa">
        <span class="f_size16 bold testo_sinistra f_arancio">Tempi Moderni  </span><span class="f_size10 italic">secoli XVIII e XIX, (principali avvenimenti: I e II rivoluzione industriale, rivoluzione francese, grande scoperte scientifiche.)</span><br /><br />  
        <p>
Con il crescente sviluppo scientifico e l&rsquo;avvenimento della <a class="link_sottolineato" href="http://www.treccani.it/enciclopedia/rivoluzione-industriale/" title="Rivoluzione Industriale" target="_blank">rivoluzione industriale</a> (iniziato nel secolo XVIII), <span class="bold">il modo in cui l&rsquo;uomo si relazion&ograve; con il cibo soffr&igrave; irrimediabilmente un grande cambiamento.</span> Nacquero le prime industrie alimentari, (come quella dello zucchero in Francia all&rsquo;inizio dell&rsquo;Ottocento).<sup>23</sup> L&rsquo;impiego delle macchine nell&rsquo;agricoltura e l&rsquo;invenzione del mulino a cilindro (usato per macinare il grano e ricavare farina) velocizz&ograve; la produzione, che, di conseguenza, fece aumentare la disponibilit&agrave; di cibo.<sup>24</sup><br /><br />
Nei paesi pi&ugrave; sviluppati <span class="bold">l&rsquo;accesso a una maggior diversit&agrave; di cibo cominci&ograve; ad essere possibile per tutti i ceti della societ&agrave;.</span> Tuttavia, esisteva ancora una grande differenza tra i pasti dei pi&ugrave; ricchi e quelli dei pi&ugrave; poveri. <span class="bold">I nobili, ad esempio, mangiavano tra le dodici e le quindici portate di pasti alla volta.</span> Un loro menu tipo era composto di minestra alla crema di riso, un primo di costolette di agnello fritto e filetti di pollo al tartufo, animelle di vitello e cicoria, filetti di anatroccolo e piselli. Come piatti intermedi, insalata di astici, uova di piviere. Come secondo, pollo al crescione con piselli. Dolci: gelatina di frutta, meringhe.<sup>25</sup><br /><br/>
In parallelo <span class="bold">le scoperte scientifiche, le preoccupazioni sanitarie e di igiene legate alla <a class="link_sottolineato" href="http://www.treccani.it/enciclopedia/conservazione-degli-alimenti/" title="Conservazione degli alimenti" target="_blank">conservazione</a> ed al consumo del cibo, permisero la prevenzione di epidemie ed una miglior alimentazione dell&rsquo;intera popolazione.</span> La sterilizzazione e la pastorizzazione, ad esempio, furono essenziali per migliorare la qualit&agrave; del cibo e farlo diventare pi&ugrave; durevole nel tempo.
Con una maggior disponibilit&agrave; di cibo il relazionarsi cambia e l&rsquo;alimentazione passa a essere definitivamente (dal punto di vista culturale) un momento dedicato alla socializzazione ed al festeggiamento, manifestando per le diverse culture una forma di ringraziamento.<br /><br /> 
Nel secolo XIX si consolida definitivamente la vita urbana e si assiste all&rsquo;invenzione del cibo conservato in latta o in bottiglie di vetro e di sistemi per la refrigerazione che,  prima di essere disponibili nelle cucine di ogni casa, furono installate sulle navi per permettere il trasporto di derrate alimentari per lunghi tragitti.<sup>26</sup> <span class="bold">La catena del freddo guadagn&ograve; cos&igrave; sempre pi&ugrave; spazio e il cibo surgelato divent&ograve; la normalit&agrave; in tutto il mondo.</span> 
D&rsquo;altro canto la cosiddetta &quot;catena del freddo&quot; si &egrave; talmente intensificata che attualmente rappresenta uno dei pi&ugrave; gravi problemi ambientali. 
	</p>
    </div>   
<!-- storia attualità -->
    <div id="intro_attualita1" class="testo_societa"><span class="f_size16 bold testo_sinistra f_arancio">Attualit&agrave;</span><span class="f_size10 italic"> secolo XX E XXI </span><br /><br />     
        <p>
A partire dal secolo XX, quando si parla di storia dell&rsquo;alimentazione si associa il concetto di stile di vita. Con l&rsquo;aumento della velocit&agrave; nei trasposti ed i cambiamenti sociali, economici e culturali avvenuti, i ritmi di vita diventavano sempre pi&ugrave; accelerati. Il processo di globalizzazione<sup>27</sup>, che ha dato contributi importanti per l&rsquo;internazionalizzazione dei prodotti alimentari, ha reso possibile gustare il cibo di altre culture senza dover necessariamente viaggiare. <span class="bold">Il progresso tecnologico avanza ed il territorio &egrave; scarsamente valorizzato,</span> costruendo uno scenario opposto a quello delineato nella storia dell&rsquo;uomo.<br /><br />
Con l&rsquo;avvento della rivoluzione industriale, la produzione del cibo cominci&ograve; ad avere proporzioni globali e le tecniche di conservazione degli alimenti furono determinanti per far arrivare il cibo a tutti i ceti della societ&agrave;.<sup>28</sup><br /><br />
<span class="bold">L&rsquo;invenzione del frigorifero domestico e la sua conseguente popolarizzazione</span> (in Italia negli anni &rsquo;50 e &rsquo;60) mette in mostra un grande cambiamento nelle abitudini della societ&agrave;, trasformazione che si fa sentire sia in termini di scelta/consumo che di produzione.<br /> 
<span class="bold">L&rsquo;industrializzazione del cibo pass&ograve; attraverso un processo di &quot;oggetificazione&quot;, nel quale fu considerato come un prodotto piuttosto che una risorsa,</span> con un conseguente declino dal punto di vista della qualit&agrave;, cambiando cos&igrave; il rapporto tra uomo e cibo.<sup>29</sup> I supermercati, infatti, rimpiazzano le drogherie e diventano luoghi per vendere generi alimentari di vario tipo con un&rsquo;esposizione dei medesimi in un lungo periodo di tempo (grazie all&rsquo;azione dei conservanti chimici non naturali).<br /><br /> 
<span class="bold">L&rsquo;evoluzione delle industrie alimentari e la necessit&agrave; di sfamare un numero sempre crescente di persone ha cos&igrave; provocato non solo problemi ambientali, ma anche per la salute dell&rsquo;individuo: basti pensare al largo impiego di composti agro-tossici e fertilizzanti chimici, utilizzati per mantenere la produzione il pi&ugrave; elevata possibile.</span>  
Dopo la Seconda Guerra Mondiale, la societ&agrave; richiedeva ritmi sempre pi&ugrave; elevati (e con lo slogan statunitense &quot;Time is Money&quot;) si vede la crescente ascesa di catene fast food.<sup>30</sup> McDonald&rsquo;s, la catena pi&ugrave; conosciuta tra queste, (nato nel 1948) introduce velocit&agrave; nella preparazione del sandwich, con l&rsquo;obbiettivo di vendere hamburger preparati nel modo pi&ugrave; veloce possibile, provocando un&rsquo;accelerazione anche nelle tempistiche della consumazione. Anche quest&rsquo;altro approccio al cibo porta a mutare ulteriormente lo stile di vita dell&rsquo;individuo, con ulteriori ripercussioni sulla propria salute e su quella del pianeta. Parallelamente, in Italia, nel finale degli anni 80&rsquo;, nasce un movimento di controtendenza e a favore della valorizzazione del territorio e della qualit&agrave; dei prodotti: Slow Food.<sup>31</sup> 
        </p>
    </div>   

