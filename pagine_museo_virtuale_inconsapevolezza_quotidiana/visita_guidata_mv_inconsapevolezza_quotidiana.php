    <p id="introduzione_societa" class="contenuto_interno">
<span class="bold">La societ&agrave; &egrave; l&rsquo;immagine riflessa di ogni singolo individuo</span> che ne fa parte ed &egrave; sempre figlia di una determinata cultura, che a sua volta &egrave; <span class="bold">caratterizzata dalle opportunit&agrave; offerte dal territorio.</span><br />
In un contesto cos&igrave; eterogeneo come quello attuale, ci&ograve; che le accomuna &egrave; ad esempio <span class="bold">il mangiare insieme, abitudine che l&rsquo;uomo ha mantenuto fin dall&rsquo;antichit&agrave;.</span> Feste e incontri sono da sempre un pretesto per condividere i pasti e socializzare.
&egrave; da notare che, considerando altri due aspetti (l&rsquo;evoluzione storica e gli stili di vita riferiti all&rsquo;attualit&agrave;), lo schema a lato si modifica ulteriormente, dando origine a una rete di relazioni ancora pi&ugrave; complessa.

