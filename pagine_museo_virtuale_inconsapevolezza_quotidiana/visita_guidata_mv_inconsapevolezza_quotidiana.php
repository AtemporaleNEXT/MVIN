               <h4 class="bold maiuscoletto">Il rapporto con il cibo nell'attualità</h4><br />
               
<span class="destacatesto">L’attuale società enfatizza una profonda contraddizione.</span> Se da un lato è vero che l’offerta di cibo a disposizione tende ad aumentare, dall’altro <span class="destacatesto">i disturbi del comportamento alimentare assumono proporzioni epidemiche.<sup>29</sup></span> Infatti <span class="destacatesto">in italia</span>, l’ISTAT registra che <span class="destacatesto">10 persone su 100 soffrono di obesità</span> e 3 su 100 sono sottopeso.<sup>30</sup><br /><br /> 

