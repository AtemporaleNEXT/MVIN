            <span id="visita_titoletto_territorio_introduzione" class="visita_titoletti_article">Il territorio, introduzione</span>
            <span id="visita_titoletto_territorio_cibo" class="visita_titoletti_article">Cibo e relazioni</span>
        </h1>
    </header>

    <p id="introduzione_territorio" class="contenuto_interno">
Per territorio si intende il contesto geografico che da spazio a una determinata societ&agrave;. Tuttavia, considerare soltanto questo aspetto &egrave; assai riduttivo, in quanto l’ambiente &egrave; una componente “viva” tanto quanto l’individuo e la societ&agrave;.
Il concetto di vita associato &egrave; da intendere in senso lato: come un qualcosa di mutevole, capace di offrire risorse naturali e spazi per le attivit&agrave; umane.<br />
Per questo motivo, quando si fa riferimento a questa “entit&agrave;” astratta (ma cos&igrave; concreta al tempo stesso), occorre parlare delle sue potenzialit&agrave; e di ci&ograve; che ha da offrire, che in questo caso specifico corrisponde al cibo.<br />

Lo schema di relazione proposto (che mette in mostra solo alcuni dei molteplici rapporti con la realt&agrave;) si trasforma ancora una volta, spostando l’attenzione dal territorio alla sua risorsa.
    </p>

