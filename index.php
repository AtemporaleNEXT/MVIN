        <header><h2 class="titoli_index bold f_nero70 font_smoothing">La nostra<span class="f_arancio bold"> mission</span></h2></header>  
	<div class="clearline"></div>
            
            <p class="testo_colonna"> 
<span>La mission del <span itemprop="name" class="bold">&quot;Museo Virtuale dell'Inconsapevolezza Quotidiana&quot;</span></span> 
<abbr title="Museo Virtuale Dell&rsquo;Inconsapevolezza Quotidiana">(MVIn)</abbr> è quella di <span class="bold">mostrare la complessa rete di relazioni
che governa la vita quotidiana</span> di ognuno e di<span class="bold"> costruire</span> (insieme al pubblico)  <span class="bold">una percezione congiunta e una maggior consapevolezza</span>, sia grazie alle conoscenze iniziali che alla complementarietà proposta dal museo. 
In questo senso, si cercherà di stimolare a <span>guardare con altri occhi la quotidianità</span>, offrendo uno spazio museale
di riferimento nel web, con straordinari percorsi interattivi e sviluppati ad hoc.<br />
<span class="nascondi1366">Il Museo propone quindi una visita improntata sulla trasversalità degli <span>argomenti trattati,</span> 
presentando in modo alternativo le <span>tematiche di carattere ambientale</span>, portando l’utente a osservare le <span class="bold">azioni quotidiane</span> 
(come mangiare, lavare, acquistare e muoversi) con una “visione in terza persona” e alcuni dei <span>macro temi</span> (inquinamento, utilizzo di 
risorse e produzione di rifiuti) che spesso vengono sviluppati in maniera sconnessa dalla realtà, attraverso una “visione in prima persona”.</span>
Inoltre, il <abbr title="Museo Virtuale Dell&rsquo;Inconsapevolezza Quotidiana">MVIn</abbr> vuole <span class="bold">stabilire un dialogo aperto e continuo con le community</span>, sviluppando un clima di reciproca fiducia, invitando chiunque sia interessato a imparare e a <span class="bold">diffondere la consapevolezza quotidiana</span>, <span class="bold">in modo tale da dare vita a un “museo di processo”</span>, dove <span>il pubblico può far parte dello staff</span>. 
            </p>
